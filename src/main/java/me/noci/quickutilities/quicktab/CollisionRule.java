package me.noci.quickutilities.quicktab;

public enum CollisionRule {

    /**
     * Always collide.
     */
    ALWAYS,
    /**
     * Only collide with other teams.
     */
    FOR_OTHER_TEAMS,
    /**
     * Only collide with own team.
     */
    FOR_OWN_TEAM,
    /**
     * Never collide.
     */
    NEVER

}
